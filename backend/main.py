@app.get("/api/date-range")
async def get_date_range():
    """
    Get the minimum and maximum dates available in the dataset.
    """
    global CLEANED_DF, DF_LOADED
    if not DF_LOADED:
        load_dataframe_in_background()
        
    if CLEANED_DF is None:
        # If dataset could not be loaded, return a sensible default range
        return {
            "min_date": "2023-11-01",
            "max_date": "2024-04-30",
        }
        
    try:
        min_date = CLEANED_DF["created_datetime"].min()
        max_date = CLEANED_DF["created_datetime"].max()
        return {
            "min_date": min_date.strftime("%Y-%m-%d") if not pd.isnull(min_date) else "2023-11-01",
            "max_date": max_date.strftime("%Y-%m-%d") if not pd.isnull(max_date) else "2024-04-30",
        }
    except Exception as e:
        print(f"Error computing date range: {e}")
        return {
            "min_date": "2023-11-01",
            "max_date": "2024-04-30",
        }


@app.get("/api/hotspots")
async def get_hotspots(start_date: str = None, end_date: str = None):
    """
    Get all parking hotspots with coordinates and scores.
    Used for both density and impact map rendering.
    """
    hotspots, _ = get_filtered_data(start_date, end_date)
    if not hotspots:
        return []
    
    # Return simplified list for map rendering
    result = []
    for h in hotspots:
        result.append({
            "zone_id": h["cluster_id"],
            "zone_name": h["zone_name"],
            "lat": h["center_lat"],
            "lon": h["center_lon"],
            "impact_score": h["impact_score"],
            "impact_rank": h["impact_rank"],
            "density_rank": h["density_rank"],
            "total_violations": h["total_violations"],
            "violation_percentile": h["violation_percentile"],
            "police_station": h["police_station"],
        })
    
    return result


@app.get("/api/hotspot/{zone_id}")
async def get_hotspot_detail(zone_id: int, start_date: str = None, end_date: str = None):
    """
    Get detailed information for a specific hotspot zone.
    Includes zone_classification and string-based recommendations.
    """
    hotspots, recommendations = get_filtered_data(start_date, end_date)
    for h in hotspots:
        if h["cluster_id"] == zone_id:
            # Get zone recommendation object (new format)
            zone_rec = None
            for r in recommendations:
                if r["zone_id"] == zone_id:
                    zone_rec = r
                    break
            
            # Extract string recommendations list
            rec_strings = []
            zone_classification = h.get("zone_classification", "Moderate Zone")
            if zone_rec:
                rec_strings = zone_rec.get("recommendations", [])
                zone_classification = zone_rec.get("zone_classification", zone_classification)
            
            return {
                "zone_id": h["cluster_id"],
                "zone_name": h["zone_name"],
                "center_lat": h["center_lat"],
                "center_lon": h["center_lon"],
                "impact_score": h["impact_score"],
                "impact_rank": h["impact_rank"],
                "density_rank": h["density_rank"],
                "total_violations": h["total_violations"],
                "violation_percentile": h["violation_percentile"],
                "impact_percentile": h.get("impact_percentile", "P25"),
                "zone_classification": zone_classification,
                "police_station": h["police_station"],
                "vehicle_distribution": h.get("vehicle_distribution", {}),
                "top_locations": h.get("top_locations", []),
                "hourly_distribution": h.get("hourly_distribution", {}),
                "junction_ratio": h.get("junction_ratio", 0),
                "avg_resolution_time": h.get("avg_resolution_time", 0),
                "violation_types": h.get("violation_types", {}),
                "top_vehicles": h.get("top_vehicles", []),
                "recommendations": rec_strings,
            }
    
    raise HTTPException(status_code=404, detail=f"Zone {zone_id} not found")


@app.get("/api/density-map")
async def get_density_map(start_date: str = None, end_date: str = None):
    """
    Get hotspots formatted for the Violation Density Map.
    Colored by violation count percentiles.
    """
    hotspots, _ = get_filtered_data(start_date, end_date)
    if not hotspots:
        return []
    
    result = []
    for h in hotspots:
        # Color based on percentile
        percentile = h.get("violation_percentile", "P25")
        if percentile == "P90":
            color = "#EF4444"    # Red - Critical
            risk = "Critical"
        elif percentile == "P75":
            color = "#F97316"    # Orange - High
            risk = "High"
        elif percentile == "P50":
            color = "#EAB308"    # Yellow - Moderate
            risk = "Moderate"
        else:
            color = "#22C55E"    # Green - Low
            risk = "Low"
        
        result.append({
            "zone_id": h["cluster_id"],
            "zone_name": h["zone_name"],
            "lat": h["center_lat"],
            "lon": h["center_lon"],
            "violations": h["total_violations"],
            "density_rank": h["density_rank"],
            "percentile": percentile,
            "color": color,
            "risk": risk,
        })
    
    return result


